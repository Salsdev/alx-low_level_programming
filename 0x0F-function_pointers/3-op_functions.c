#include "3-calc.h"
/**
 * op_add - This function calculates the sum of two integers
 * @a: This is the first integer
 * @b: This is the second integer
 *
 * Return: It will return the sum of a and b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - This function calculates the difference of two integers
 * @a: This is the first integer
 * @b: This is the second integer
 *
 * Return: Will provide the difference of a and b
 */
 int op_sub(int a, int b)
{
		return (a - b);
}

/**
 * op_mul - This function calculates the product of two integers
 * @a: This is the first integer
 * @b: This is the second integer
 *
 * Return: This will provide the product of a and b
 */
int op_mul(int a, int b)
{
		return (a * b);
}

/**
 * op_div - This function will calculate the division of two integers
 * @a: This is the first integer
 * @b: This is the second integer
 *
 * Return: Will provide the result of the division of a and b
 */
int op_div(int a, int b)
{
		return (a / b);
}

/**
 * op_mod - This function will calculate the remainder of
 * the division of two integers
 * @a: This is the first integer
 * @b: This is the second integer
 *
 * Return: Provide the remainder of a divided b
 */
int op_mod(int a, int b)
{
		return (a % b);
}
